FALLBACK = [
    "Какой у тебя был лучший день за последний месяц?",
    "Если бы можно было прожить год где угодно — где бы это было?",
    "Что-то, чем сейчас всерьёз увлечён(а)?",
    "Маленькая привычка, которая делает день лучше?",
    "Самый странный комплимент, который тебе делали?",
]


def pick_topics(interests_a: list[str] | None, interests_b: list[str] | None) -> tuple[list[str], str]:
    """Return up to 3 topics + the category label used (for metrics)."""
    a = {i.lower().strip() for i in (interests_a or [])}
    b = {i.lower().strip() for i in (interests_b or [])}
    common = [i for i in a & b if i in TEMPLATES]

    chosen: list[str] = []
    category = "fallback"

    if common:
        category = random.choice(common)
        chosen = random.sample(TEMPLATES[category], k=min(3, len(TEMPLATES[category])))
    else:
        chosen = random.sample(FALLBACK, k=3)

    icebreaker_sent_total.labels(category=category).inc()
    return chosen, category

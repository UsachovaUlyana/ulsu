-- ============================================================
-- АНОМАЛИЯ 4: LOST UPDATE (Потерянное обновление)
-- ============================================================
-- Две транзакции читают одно и то же значение, затем обе
-- его изменяют. Результат второй транзакции перезаписывает
-- результат первой — обновление первой транзакции "теряется".
--
-- Уровень изоляции: READ COMMITTED
-- ============================================================

-- === Подготовка (сброс данных) ===
UPDATE accounts SET balance = 50000.00 WHERE owner = 'Иванов Алексей';

-- ============================================================
-- Сценарий: Два оператора одновременно пополняют счёт.
-- Оператор 1 добавляет +15000 (зарплата)
-- Оператор 2 добавляет +30000 (перевод)
-- Ожидаемый результат: 50000 + 15000 + 30000 = 95000
-- ============================================================

-- ============================================================
-- Шаг 1 (Терминал 1 — Транзакция A, Оператор 1):
-- ============================================================
BEGIN;
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;

SELECT balance FROM accounts WHERE owner = 'Иванов Алексей';
-- Результат: balance = 50000.00

-- ============================================================
-- Шаг 2 (Терминал 2 — Транзакция B, Оператор 2):
-- В ДРУГОМ терминале выполняем:
-- ============================================================
/*
BEGIN;
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;

SELECT balance FROM accounts WHERE owner = 'Иванов Алексей';
-- Результат: balance = 50000.00

-- Оператор 2 добавляет 30000
UPDATE accounts SET balance = 80000.00 WHERE owner = 'Иванов Алексей';
COMMIT;
*/

-- ============================================================
-- Шаг 3 (Терминал 1 — Транзакция A):
-- Оператор 1 добавляет 15000 (думает, что баланс всё ещё 50000)
-- ============================================================
UPDATE accounts SET balance = 65000.00 WHERE owner = 'Иванов Алексей';
COMMIT;

-- Проверяем результат
SELECT * FROM accounts WHERE owner = 'Иванов Алексей';
-- Результат: balance = 65000.00  ← LOST UPDATE!
-- Ожидалось: 50000 + 15000 + 30000 = 95000
-- Получено:  65000 (перевод в 30000 потерян!)

-- ============================================================
-- АНОМАЛИЯ 2: NON-REPEATABLE READ (Неповторяемое чтение)
-- ============================================================
-- Транзакция читает одну и ту же строку дважды и получает
-- разные значения, потому что другая транзакция успела
-- изменить и зафиксировать данные между двумя чтениями.
--
-- Уровень изоляции: READ COMMITTED (по умолчанию в PostgreSQL)
-- ============================================================

-- === Подготовка (сброс данных) ===
UPDATE accounts SET balance = 50000.00 WHERE owner = 'Иванов Алексей';

-- ============================================================
-- Шаг 1 (Терминал 1 — Транзакция A):
-- ============================================================
BEGIN;
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;

-- Первое чтение
SELECT * FROM accounts WHERE owner = 'Иванов Алексей';
-- Результат: balance = 50000.00

-- ============================================================
-- Шаг 2 (Терминал 2 — Транзакция B):
-- В ДРУГОМ терминале выполняем:
-- ============================================================
/*
BEGIN;
UPDATE accounts SET balance = 95000.00 WHERE owner = 'Иванов Алексей';
COMMIT;
*/

-- ============================================================
-- Шаг 3 (Терминал 1 — Транзакция A):
-- Повторное чтение — значение уже другое!
-- ============================================================
SELECT * FROM accounts WHERE owner = 'Иванов Алексей';
-- Результат: balance = 95000.00  ← NON-REPEATABLE READ!

COMMIT;

-- ============================================================
-- ИТОГ:
-- Первый раз:  balance = 50000.00
-- Второй раз:  balance = 95000.00
-- Значения отличаются внутри одной транзакции!
-- ============================================================

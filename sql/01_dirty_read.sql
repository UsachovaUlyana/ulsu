-- ============================================================
-- АНОМАЛИЯ 1: DIRTY READ (Грязное чтение)
-- ============================================================
-- Транзакция читает данные, которые были изменены другой
-- транзакцией, но ещё НЕ зафиксированы (COMMIT не выполнен).
-- Если первая транзакция откатится (ROLLBACK), вторая получит
-- "грязные" данные, которых никогда не существовало.
--
-- Уровень изоляции: READ UNCOMMITTED
-- ============================================================

-- === Подготовка (сброс данных) ===
UPDATE accounts SET balance = 50000.00 WHERE owner = 'Иванов Алексей';

-- ============================================================
-- Шаг 1 (Терминал 1 — Транзакция A):
-- Начинаем транзакцию и изменяем баланс, но НЕ коммитим
-- ============================================================
BEGIN;
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED;

UPDATE accounts SET balance = 500000.00 WHERE owner = 'Иванов Алексей';
-- НЕ выполняем COMMIT! Данные ещё не зафиксированы.

-- ============================================================
-- Шаг 2 (Терминал 2 — Транзакция B):
-- В ДРУГОМ терминале выполняем:
-- ============================================================
/*
BEGIN;
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED;

SELECT * FROM accounts WHERE owner = 'Иванов Алексей';
-- При dirty read: balance = 500000.00  ← ГРЯЗНОЕ ЧТЕНИЕ!
-- Транзакция B видит незафиксированные данные транзакции A.

COMMIT;
*/

-- ============================================================
-- Шаг 3 (Терминал 1 — Транзакция A):
-- Откатываем изменения — баланс 500000 НИКОГДА не существовал
-- ============================================================
ROLLBACK;

-- Проверяем реальное состояние
SELECT * FROM accounts WHERE owner = 'Иванов Алексей';
-- Результат: balance = 50000.00

-- ============================================================
-- ПРИМЕЧАНИЕ для PostgreSQL:
-- PostgreSQL НЕ допускает dirty read даже при READ UNCOMMITTED.
-- Он автоматически повышает уровень до READ COMMITTED.
-- Для наглядной демонстрации dirty read можно использовать MySQL.
-- ============================================================

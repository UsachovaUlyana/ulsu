-- ============================================================
-- АНОМАЛИЯ 3: PHANTOM READ (Фантомное чтение)
-- ============================================================
-- Транзакция выполняет один и тот же запрос с условием WHERE
-- дважды, но получает РАЗНОЕ количество строк, потому что
-- другая транзакция вставила новые строки, удовлетворяющие
-- условию.
--
-- Уровень изоляции: READ COMMITTED
-- ============================================================

-- === Подготовка (удаляем строки от прошлых запусков) ===
DELETE FROM products WHERE name = 'Google Pixel 9 Pro';

-- ============================================================
-- Шаг 1 (Терминал 1 — Транзакция A):
-- ============================================================
BEGIN;
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;

-- Считаем количество смартфонов и общую стоимость
SELECT COUNT(*) AS total, SUM(price) AS total_price
FROM products
WHERE category = 'Смартфоны';
-- Результат: total = 3, total_price = 294970.00

SELECT name, price FROM products WHERE category = 'Смартфоны' ORDER BY price;

-- ============================================================
-- Шаг 2 (Терминал 2 — Транзакция B):
-- В ДРУГОМ терминале выполняем:
-- ============================================================
/*

-- ============================================================
-- Шаг 3 (Терминал 1 — Транзакция A):
-- Повторяем ТОТ ЖЕ запрос — появилась "фантомная" строка!
-- ============================================================
SELECT COUNT(*) AS total, SUM(price) AS total_price
FROM products
WHERE category = 'Смартфоны';
-- Результат: total = 4, total_price = 379960.00  ← PHANTOM READ!

SELECT name, price FROM products WHERE category = 'Смартфоны' ORDER BY price;
-- Появилась строка 'Google Pixel 9 Pro' — это фантом!

COMMIT;

